package swVo;

public class ExpCateVo {
	private int expCateIdx;
	private String expCateTitle;

	public ExpCateVo(int expCateIdx, String expCateTitle) {
		this.expCateIdx = expCateIdx;
		this.expCateTitle = expCateTitle;
	}

	public int getExpCateIdx() {
		return expCateIdx;
	}

	public void setExpCateIdx(int expCateIdx) {
		this.expCateIdx = expCateIdx;
	}

	public String getExpCateTitle() {
		return expCateTitle;
	}

	public void setExpCateTitle(String expCateTitle) {
		this.expCateTitle = expCateTitle;
	}
	
	
}
